package com.date.DesignPattern.结构型.适配器模式.对象适配器模式;

public class CockAdapter implements Duck {

    Cock cock;
    // ??????????????????????????????????????????????
    public CockAdapter(Cock cock) {
        this.cock = cock;
    }

    @Override
    public void quack() {
        cock.gobble();

    }

    @Override
    public void fly() {
        cock.fly();

    }
}
